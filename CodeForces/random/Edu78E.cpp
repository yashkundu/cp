const int N = 5e5+10;
vector<int> g[N];
pair<int, int> ans[N];

int n;


int dfs(int v, int p, int start, int lastOcc) {
    int cnt = g[v].size();
    if(p!=-1) cnt--;
    ans[v] = {start, lastOcc+cnt+1};
    int newLastOcc = lastOcc+cnt+1;
    int f = 0;
    for(int u: g[v]) {
        if(u==p) continue;
        newLastOcc = dfs(u, v, lastOcc+cnt-f, newLastOcc);
        f++;
    }
    return newLastOcc;
}

 
void solve() {
    cin >> n;
    for(int i=0;i<n-1;i++) {
        int x, y;
        cin >> x >> y;
        x--, y--;
        g[x].push_back(y);
        g[y].push_back(x);
    }

    dfs(0, -1, 1, 1);

    for(int i=0;i<n;i++) {
        cout << ans[i].first << " " << ans[i].second << "\n";
    }


    
}
 
signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
 
    int t = 1;
    // cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}
