ll csxor(ll x) {
    if(x<=1) return x;
    ll ans = 1;
    if(((x-1)/2) & 1) ans ^= 1;
    if(x%2==0) ans ^= x;
    return ans;
}


ll query(ll x, ll k, ll i) {
    if(x==0) return 0;
    ll ans = csxor(x);
    if(k>x) return ans;

    ll num = (x-k)/(1LL<<i);
    ll other = csxor(num)<<i;
    ll del = num;
    if(k) del++;
    if(del&1) other ^= k;
    ans ^= other;
    return ans;
}

ll l, r, i, k;

 
void solve() {
    cin >> l >> r >> i >> k;

    cout << (query(r, k, i)^query(l-1, k, i)) << "\n";
}
 
