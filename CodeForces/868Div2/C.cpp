void solve() {
    int n;
    cin >> n;
    map<int, int> mp;
    for(int i=0;i<n;i++) {
        int x;
        cin >> x;
        for(int i=2;i*i<=x;i++) {
            if(x%i==0) {
                while(x%i==0) {
                    mp[i]++;
                    x /= i;
                }
            }
        }
        if(x>1) {
            mp[x]++;
        }

    }

    int ans = 0;
    int ones = 0;

    for(auto it=mp.begin();it!=mp.end();it++) {
        int cnt = it->second;
        ans += cnt/2;
        if(cnt&1) {
            ones++;
        }
    }

    ans += ones/3;
    cout << ans << "\n";


}
 
signed main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
 
    int t = 1;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}
