int n, m;

int calcOr(int l, int r) {
    int ans = l;
    int cur = l;
    for(int j=0;j<=30;j++) {
        if(cur&(1<<j)) {
            cur -= (1<<j);
        } else {
            cur += (1<<j);
            if(cur<=r) {
                ans |= (1<<j);
                cur -= (1<<j);
            } else {
                break;
            }
        }
    }
    return ans;
}
 
 
void solve() {
    cin >> n >> m;


    int ans = calcOr(max(0, n-m), n) | calcOr(n, n+m);
    cout << ans << "\n";

}
 
