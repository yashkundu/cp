int x, y; 
int cnt = 0;

void solve() {
    cin >> x >> y;
    cnt = 0;

    while(x!=0 || y!=0) {
        if(x%2!=y%2) break;
        cnt++;
        x = x>>1;
        y = y>>1;
    }

    cout << (1<<cnt) << "\n";


}
 
