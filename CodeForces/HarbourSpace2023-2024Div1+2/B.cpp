void solve() {
    int n, k;
    cin >> n >> k;
    string s;
    cin >> s;
    vector<char> c0, c1;
    for(int i=0;i<s.size();i++) {
        if(i&1) c1.push_back(s[i]);
        else c0.push_back(s[i]);
    }
    sort(c0.begin(), c0.end());
    sort(c1.begin(), c1.end());

    if(k&1) {
        for(int i=0;i<n;i++) {
            if(i%2==0) cout << c0[i/2];
            else cout << c1[i/2];
        }
        cout << "\n";
        return;
    }

    sort(s.begin(), s.end());
    cout << s << "\n";


}
 
